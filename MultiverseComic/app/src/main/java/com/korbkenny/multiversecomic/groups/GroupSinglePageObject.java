public class GroupSinglePageObject {
    private String mBeingWorkedOn, mFrom, mFromUser, mImage, mText, mImageUser, mNext, mButtonText, mButtonUser;

    public GroupSinglePageObject() {}

    public String getBeingWorkedOn() {
        return mBeingWorkedOn;
    }

    public void setBeingWorkedOn(String beingWorkedOn) {
        mBeingWorkedOn = beingWorkedOn;
    }

    public String getFrom() {
        return mFrom;
    }

    public void setFrom(String from) {
        mFrom = from;
    }

    public String getFromUser() {
        return mFromUser;
    }

    public void setFromUser(String fromUser) {
        mFromUser = fromUser;
    }

    public String getImage() {
        return mImage;
    }

    public void setImage(String image) {
        mImage = image;
    }

    public String getText() {
        return mText;
    }

    public void setText(String text) {
        mText = text;
    }

    public String getImageUser() {
        return mImageUser;
    }

    public void setImageUser(String imageUser) {
        mImageUser = imageUser;
    }

    public String getNext() {
        return mNext;
    }

    public void setNext(String next) {
        mNext = next;
    }

    public String getButtonText() {
        return mButtonText;
    }

    public void setButtonText(String buttonText) {
        mButtonText = buttonText;
    }

    public String getButtonUser() {
        return mButtonUser;
    }

    public void setButtonUser(String buttonUser) {
        mButtonUser = buttonUser;
    }
}
